a = [1, 2, 3, 4, 5, 6, 7, 8, 9]

# Get elements starting from index 2
# to the end of the list
b = a[2:]
print(b)

#Example2

a = [1, 2, 3, 4, 5, 6, 7, 8, 9]
# Get elements starting from index 0 
# to index 3 (excluding 3th index)
c = a[:3]
print(c)

#Example3

a = [1, 2, 3, 4, 5, 6, 7, 8, 9]
# Get every second element from the list
# starting from the beginning
b = a[::2]
print(b)